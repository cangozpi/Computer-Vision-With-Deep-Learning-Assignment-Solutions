    ##############################################################################
    # *****START OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****

    T = x.shape[1]
    # iterate through every element in the sequences since all are of same length
    h = np.zeros((x.shape[0], T, h0.shape[1])) # fill with calculated values
    cache = list() # holds caches corresponding to vector in T
