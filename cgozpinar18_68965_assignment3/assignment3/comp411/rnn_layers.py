def rnn_backward(dh, cache):
    """
    Compute the backward pass for a vanilla RNN over an entire sequence of data.

    Inputs:
    - dh: Upstream gradients of all hidden states, of shape (N, T, H). 
    
    NOTE: 'dh' contains the upstream gradients produced by the 
    individual loss functions at each timestep, *not* the gradients
    being passed between timesteps (which you'll have to compute yourself
    by calling rnn_step_backward in a loop).

    Returns a tuple of:
    - dx: Gradient of inputs, of shape (N, T, D)
    - dh0: Gradient of initial hidden state, of shape (N, H)
    - dWx: Gradient of input-to-hidden weights, of shape (D, H)
    - dWh: Gradient of hidden-to-hidden weights, of shape (H, H)
    - db: Gradient of biases, of shape (H,)
    """
    dx, dh0, dWx, dWh, db = None, None, None, None, None
    ##############################################################################
    # TODO: Implement the backward pass for a vanilla RNN running an entire      #
    # sequence of data. You should use the rnn_step_backward function that you   #
    # defined above. You can use a for loop to help compute the backward pass.   #
    ##############################################################################
    # *****START OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    
    # extract dimensions to initialize partial derivative variables
    N, T, H = dh.shape
    x, prev_h, Wx, Wh, b, next_h = cache[0]
    D = x.shape[1]

    # variables to hold the backpropagated partial derivatives
    dx = np.zeros((N, T, D)) 
    dh0 = np.zeros((N, H)) 
    dWx = np.zeros((D, H)) 
    dWh = np.zeros((H, H)) 
    db = np.zeros((H))

     
    dnext_h = None # (N, H) # holds t^th cache
    for t in range(T-1, -1, -1): # go in reverse order since we backpropagate through time
        if t ==  T-1:# upstream gradient equals the gradient flow from upstream
            dnext_h = dh[:,t,:]
            cache_current = cache[t]
            dx_current, dprev_h_current, dWx_current, dWh_current, db_current = rnn_step_backward(dnext_h, cache_current)
            # update the gradients
            dx[:,t,:] += dx_current
            dWx += dWx_current
            dWh += dWh_current
            db += db_current
            # pass the gradient of the hidden state to earlier iterations
            dnext_h = dprev_h_current
        else:
            # propagate upstream gradient
            # use dnext_h from previous time step since it holds upstream gradient
            cache_current = cache[t]
            dx_current1, dprev_h_current1, dWx_current1, dWh_current1, db_current1 = rnn_step_backward(dnext_h, cache_current)
            # calculate gradient wrt current time step's Loss
            dnext_h_current_loss = dh[:,t,:] # gradient of current time step's Loss wrt next_h
            dx_current2, dprev_h_current2, dWx_current2, dWh_current2, db_current2 = rnn_step_backward(dnext_h_current_loss, cache_current)
            
            # update the gradients
            dx[:,t,:] += dx_current1 + dx_current2
            dWx += dWx_current1 + dWx_current2
            dWh += dWh_current1 + dWh_current2
            db += db_current1 + db_current2
            # pass the gradient of the hidden state to earlier iterations
            dnext_h = dprev_h_current1 + dprev_h_current2 
            
            if t == 0: # last time step so calculate dh0
                dh0 = dnext_h
    # *****END OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    ##############################################################################
    #                               END OF YOUR CODE                             #
    ##############################################################################
    return dx, dh0, dWx, dWh, db


